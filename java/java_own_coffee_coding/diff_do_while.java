public class diff_do_while{

	public static void main(String[]args){

		int i = 12; // Initializing the value of `i`

		// Applying the while loop concept first

		while(i <= 11){
			
			System.out.print(i + " from while loop ");
			i++;
		}


		// Applying the do while loop concept second

		do{
			System.out.print(i + " from do while loop ");
			i++;

		}while(i <= 11);
	}
}

